package entity;

public class Project {
private boolean proBool;
private int proId;
private String proName;
private int proNumber;
private String proShow;
public boolean isBool() {
		return proBool;
}
public boolean isProBool() {
	return true;
}
public void setProBool(boolean proBool) {
	this.proBool = proBool;
}
public int getProId() {
	return proId;
}
public void setProId(int proId) {
	this.proId = proId;
}
public String getProName() {
	return proName;
}
public void setProName(String proName) {
	this.proName = proName;
}
public int getProNumber() {
	return proNumber;
}
public void setProNumber(int proNumber) {
	this.proNumber = proNumber;
}
public String getProShow() {
	return proShow;
}
public void setProShow(String proShow) {
	this.proShow = proShow;
}

}
