package entity;

public class Recoder {
private String chooseName;
private String chooseProject;
private int recoderId;
public Recoder() {
	
}
public String getChooseName() {
	return chooseName;
}
public void setChooseName(String chooseName) {
	this.chooseName = chooseName;
}
public String getChooseProject() {
	return chooseProject;
}
public void setChooseProject(String chooseProject) {
	this.chooseProject = chooseProject;
}
public int getRecoderId() {
	return recoderId;
}
public void setRecoderId(int recoderId) {
	this.recoderId = recoderId;
}
}
